package Week11;

public class WhileLoop_01 {
    public static void main(String[] args) {
       
        
        // update รอบ
        int count = 1;   // ค่าเริ่มต้น
        while (count <= 5) {   // เงื่อนไข
            System.out.println("รอบที่ "+count+" Hello");   // ค่าที่ต้องการแสดงผล
            count++;    // update รอบ
        }
    }
}
